# Estrutura de Dados - TIPOS COMPOSTOS

# Lista
lista = [1, 2, 3, 4, 5]

# Tupla
tupla = (1, 2, 3, 4, 5)

# Dicionário -> Se assemelha ao Map em Dart
dicionario = {"nome": "João", "idade": 25}

# Conjunto
conjunto = {1, 2, 3, 4, 5}

# Conjunto Imutável
conjunto_imutavel = frozenset([1, 2, 3, 4, 5])

# Complexo
complexo = 3 + 4j

# Nenhum
nenhum = None

# Imprimindo os valores
print(type(lista))
print(type(tupla))
print(type(dicionario))
print(type(conjunto))
print(type(conjunto_imutavel))
print(type(complexo))
print(type(nenhum))
